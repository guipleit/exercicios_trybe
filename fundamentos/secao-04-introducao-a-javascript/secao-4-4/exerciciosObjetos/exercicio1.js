function sum (num1, num2) {
    return num1 + num2
}
console.log(sum(4, 50));

function subtraction (num1, num2) {
    return num1 - num2
}
console.log(subtraction(3, 1));

function multiplication (num1, num2) {
    return num1 * num2
}
console.log(multiplication(5, 10));

function division(num1, num2) {
    return num1 / num2
}
console.log(division(5, 4));

function modulus(num1, num2) {
    return num1 % num2
}
console.log(modulus(2, 2));